//Looping sructures - Looping structures are used in programming to repeat a block of code multiple times.

//for loop - The for loop is a commonly used loop when you know the number of times you want to execute a block of code. 
for( let i = 1; i <= 10; i++){
    console.log(i)
}


//while loop
let a=1;

while( a<=10 ){
    console.log(a);
    a++;
}

//In a do while loop, the code inside the loop is executed at least once. 
let b=1;

do{
    console.log(b);
    b++;
}while(b <= 10)